using PrinterSystem;
using PrinterSystem.Mediator;

Console.OutputEncoding = System.Text.Encoding.UTF8;
Console.WriteLine("====================================================");
Console.WriteLine("   Система управления очередью печати документов");
Console.WriteLine("====================================================\n");

// ── Инициализация компонентов ────────────────────────────────────────────
var printer    = new Printer();
var queue      = new PrintQueue();
var logger     = new Logger();
var mediator   = new PrintMediatorImpl(printer, queue, logger);
var dispatcher = new Dispatcher(mediator);

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 1: Добавление документов в очередь
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine(">>> Шаг 1: Добавление документов в очередь\n");

var doc1 = dispatcher.CreateAndEnqueue("Отчёт_Q1.pdf");
var doc2 = dispatcher.CreateAndEnqueue("Договор_№42.docx");
var doc3 = dispatcher.CreateAndEnqueue("Инструкция.txt");

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 2: Успешная печать первого документа
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine("\n>>> Шаг 2: Успешная печать первого документа\n");
dispatcher.PrintNext();

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 3: Имитация ошибки принтера на втором документе
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine("\n>>> Шаг 3: Имитация ошибки принтера (документ 2)\n");
dispatcher.PrintNextWithError();

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 4: Повторная постановка проблемного документа в очередь (Retry)
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine("\n>>> Шаг 4: Повторная постановка Договор_№42.docx в очередь\n");
dispatcher.Retry(doc2);

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 5: Дальнейшая печать (Инструкция, затем повторный Договор)
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine("\n>>> Шаг 5: Печать оставшихся документов\n");
dispatcher.PrintNext(); // Инструкция.txt
dispatcher.PrintNext(); // Договор_№42.docx (повтор)
dispatcher.PrintNext(); // Очередь пуста

// ─────────────────────────────────────────────────────────────────────────
// ШАГ 6: Проверка граничных состояний
// ─────────────────────────────────────────────────────────────────────────
Console.WriteLine("\n>>> Шаг 6: Проверка граничных состояний\n");

Console.WriteLine("  Попытка добавить уже напечатанный документ в очередь:");
doc1.AddToQueue(); // Done — недоступно

Console.WriteLine("\n  Итоговые состояния:");
Console.WriteLine($"    {doc1.Name} → {doc1.StateName}");
Console.WriteLine($"    {doc2.Name} → {doc2.StateName}");
Console.WriteLine($"    {doc3.Name} → {doc3.StateName}");

// ─────────────────────────────────────────────────────────────────────────
// Полный журнал
// ─────────────────────────────────────────────────────────────────────────
logger.PrintAll();
